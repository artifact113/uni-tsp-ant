using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace WindowsFormsApplication1
{
    class CVector2f
    {
        public float x;
        public float y;

        public CVector2f()
        {
            x = 0;
            y = 0;
        }

        public CVector2f(float X, float Y)
        {
            x = X;
            y = Y;
        }
    }
}
